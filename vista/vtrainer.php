                <p>Remo con barra (Barbell Rows): Trabaja la espalda y los bíceps. Asegúrate de mantener una buena postura y usar un peso adecuado.

                Curl de bíceps (Bicep Curls): Los curls de bíceps son excelentes para desarrollar los músculos de los brazos. Puedes hacerlos con mancuernas o una barra.

                Extensiones de tríceps (Tricep Extensions): Estos ejercicios se enfocan en los tríceps y pueden realizarse con una cuerda, una barra o mancuernas.

                Plancha (Plank): Fortalece el núcleo y mejora la estabilidad. Mantén la posición de plancha durante un tiempo específico y trata de aumentar gradualmente la duración.</p>
