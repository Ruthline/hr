                <div class="text-trainer">
                    <p>Sentadillas (Squats): Las sentadillas son uno de los ejercicios más efectivos para desarrollar los músculos de las piernas y glúteos. Asegúrate de mantener una buena forma, manteniendo la espalda recta y bajando tus caderas hasta que tus muslos estén paralelos al suelo.

                    Press de banca (Bench Press): Este ejercicio se centra en el desarrollo de los músculos del pecho, tríceps y hombros. Asegúrate de que la barra esté bien equilibrada y utiliza un compañero o equipo de seguridad para evitar lesiones.

                    Peso muerto (Deadlift): El peso muerto trabaja todo el cuerpo, pero se enfoca especialmente en la espalda baja, glúteos, piernas y músculos de la parte superior de la espalda. Es importante mantener una buena técnica para evitar lesiones.

                    Flexiones de brazos (Push-Ups): Las flexiones son un excelente ejercicio para trabajar el pecho, los tríceps y los hombros, y se pueden realizar en cualquier lugar. Comienza con flexiones desde las rodillas si no puedes hacerlas desde los pies.

                    Dominadas o Pull-Ups: Estos ejercicios trabajan la espalda, bíceps y hombros. Puedes utilizar una banda elástica o un asistente si no puedes hacer pull-ups completas al principio.

                    Press de hombros (Shoulder Press): Este ejercicio se enfoca en los deltoides y los músculos del hombro. Puedes hacerlo sentado o de pie con una barra o mancuernas.</p>
